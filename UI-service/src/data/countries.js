// Helper functions
export const getAllCountries = () => {
  return Object.entries(countriesData).map(([key, country]) => ({
    value: key,
    label: country.name
  }));
};

export const getCitiesByCountry = (countryKey) => {
  const country = countriesData[countryKey];
  if (!country) return [];
  
  return country.cities.map(city => ({
    name: city.name,
    value: city.value,
    displayName: city.name,
    lat: city.lat,
    lng: city.lng
  }));
};

export const getAllCities = () => {
  const allCities = [];
  for (const countryKey in countriesData) {
    const country = countriesData[countryKey];
    allCities.push(...country.cities.map(city => ({
      name: city.name,
      value: city.value,
      displayName: city.name,
      lat: city.lat,
      lng: city.lng
    })));
  }
  return allCities;
};

export const findCityCoordinates = (cityName) => {
  if (!cityName.trim()) return null;
  
  const normalizedCity = cityName.toLowerCase().trim();
  
  // Search through all countries for the city
  for (const countryKey in countriesData) {
    const country = countriesData[countryKey];
    const city = country.cities.find(c => c.value === normalizedCity);
    if (city) {
      return { lat: city.lat, lng: city.lng };
    }
  }
  
  return null;
};
